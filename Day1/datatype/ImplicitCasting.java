/*Ép kiểu rộng */

package datatype;

public class ImplicitCasting {
    public static void main(String[] args) {
        /*
         * Ép kiểu rộng
         */
        int i = 100;    
        long l = 600;     // chuyển từ kiểu dữ liệu integer lên kiểu long
        float f = 500;    // chuyển từ kiểu dữ liệu long lên kiểu float
        System.out.println("Giá trị biến i = " + i);
        System.out.println("Giá trị biến l = " + l);
        System.out.println("Giá trị biến f = " + f);
    }
}
