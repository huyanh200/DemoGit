package Day6.VongLap;

public class vonglapwhile {
    public static void main(String[] args) {


int count = 1;
while (count <= 5) {
    System.out.println("Giá trị của count: " + count);
    count++;
}

int num = 10;
while (num > 0) {
    System.out.println("Giá trị của num: " + num);
    num -= 2;
}
    }
}